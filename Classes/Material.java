

public class Material {

	private String name,color;

	//Constuctor
	public Material(String name, String color) {
		this.name = name;
		this.color = color;
	}

	//Getters&Setters
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}
	
	
	
}
